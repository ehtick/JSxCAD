test('Fetch header', (t) => {
  const fetcher = createByteFetcher(readFileSync('test.dst'));
  const header = fetchHeader({}, fetcher);
  t.deepEqual(header, {
    label: 'Untitled',
    stitchCount: 92,
    colorCount: 3,
    positiveX: 4100,
    negativeX: 0,
    positiveY: 0,
    negativeY: 3100,
    deltaX: undefined,
    deltaY: undefined,
    previousFile: undefined,
    previousX: undefined,
    previousY: undefined,
  });
});

